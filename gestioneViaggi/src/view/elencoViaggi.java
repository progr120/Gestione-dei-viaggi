    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        Viaggi v = new Viaggi();
        ViaggiDAO dao = new ViaggiDAO();

        v.setDestinazione(txtDest.getText());
        v.setDataPartenza(txtDataP.getText());
        v.setDataRitorno(txtDataR.getText());
        v.setPostiDisponibili(Integer.parseInt(txtPosti.getText()));
        dao.create(v);

        txtId.setText("");
        txtDest.setText("");
        txtDataP.setText("");
        txtDataR.setText("");
        txtPosti.setText("");

        readJTable();
    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        if (tblViaggi.getSelectedRow() != -1) {

            Viaggi v = new Viaggi();
            ViaggiDAO dao = new ViaggiDAO();

            v.setIdViaggio((int) tblViaggi.getValueAt(tblViaggi.getSelectedRow(), 0));
            dao.delete(v);

            txtId.setText("");
            txtDest.setText("");
            txtDataP.setText("");
            txtDataR.setText("");
            txtPosti.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para deletar");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (tblViaggi.getSelectedRow() != -1) {

            Viaggi v = new Viaggi();
            ViaggiDAO dao = new ViaggiDAO();

            v.setDestinazione(txtDest.getText());
            v.setDataPartenza(txtDataP.getText());
            v.setDataRitorno(txtDataR.getText());
            v.setPostiDisponibili(Integer.parseInt(txtPosti.getText()));
            v.setIdViaggio((int) tblViaggi.getValueAt(tblViaggi.getSelectedRow(), 0));
            dao.update(v);

            txtId.setText("");
            txtDest.setText("");
            txtDataP.setText("");
            txtDataR.setText("");
            txtPosti.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para atualizar");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

