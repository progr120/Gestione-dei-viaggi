    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tblViaggi.getModel();
        modelo.setNumRows(0);
        ViaggiDAO vdao = new ViaggiDAO();

        for (Viaggi v : vdao.read()) {
            modelo.addRow(new Object[]{
                v.getIdViaggio(),
                v.getDestinazione(),
                v.getDataPartenza(),
                v.getDataRitorno(),
                v.getPostiDisponibili()
            });
        }
    }

    public void readJTableForDest(String dest) {
        DefaultTableModel modelo = (DefaultTableModel) tblViaggi.getModel();
        modelo.setNumRows(0);
        ViaggiDAO vdao = new ViaggiDAO();

        for (Viaggi v : vdao.readForDest(dest)) {
            modelo.addRow(new Object[]{
                v.getIdViaggio(),
                v.getDestinazione(),
                v.getDataPartenza(),
                v.getDataRitorno(),
                v.getPostiDisponibili()
            });
        }
    }

