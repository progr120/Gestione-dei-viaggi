    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tblTurista.getModel();
        modelo.setNumRows(0);
        TuristiDAO tdao = new TuristiDAO();

        for (Turisti t : tdao.read()) {
            modelo.addRow(new Object[]{
                t.getIdTurista(),
                t.getNome(),
                t.getCognome(),
                t.getDataNascita(),
                t.getNazionalita(),
                t.getEmail(),
                t.getDocumento()
            });
        }
    }

    public void readJTableForDest(String nome) {
        DefaultTableModel modelo = (DefaultTableModel) tblTurista.getModel();
        modelo.setNumRows(0);
        TuristiDAO tdao = new TuristiDAO();

        for (Turisti t : tdao.readForDest(nome)) {
            modelo.addRow(new Object[]{
                t.getIdTurista(),
                t.getNome(),
                t.getCognome(),
                t.getDataNascita(),
                t.getNazionalita(),
                t.getEmail(),
                t.getDocumento()
            });
        }
    }

