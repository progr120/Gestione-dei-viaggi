    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        if (tblTurista.getSelectedRow() != -1) {

            Turisti t = new Turisti();
            TuristiDAO dao = new TuristiDAO();

            t.setIdTurista((int) tblTurista.getValueAt(tblTurista.getSelectedRow(), 0));
            dao.delete(t);

            txtIdTurista.setText("");
            txtNome.setText("");
            txtCognome.setText("");
            txtDataNascita.setText("");
            txtNazionalita.setText("");
            txtEmail.setText("");
            txtDocumento.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para deletar");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        if (tblTurista.getSelectedRow() != -1) {

            Turisti t = new Turisti();
            TuristiDAO dao = new TuristiDAO();

            t.setNome(txtNome.getText());
            t.setCognome(txtCognome.getText());
            t.setDataNascita(txtDataNascita.getText());
            t.setNazionalita(txtNazionalita.getText());
            t.setEmail(txtEmail.getText());
            t.setDocumento(txtDocumento.getText());
            t.setIdTurista((int) tblTurista.getValueAt(tblTurista.getSelectedRow(), 0));
            dao.update(t);

            txtIdTurista.setText("");
            txtNome.setText("");
            txtCognome.setText("");
            txtDataNascita.setText("");
            txtNazionalita.setText("");
            txtEmail.setText("");
            txtDocumento.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para atualizar");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

