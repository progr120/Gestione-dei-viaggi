    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tblPartecipanti.getModel();
        modelo.setNumRows(0);
        PartecipantiDAO pdao = new PartecipantiDAO();

        for (Partecipanti p : pdao.read()) {
            modelo.addRow(new Object[]{
                p.getIdPartecipanti(),
                p.getIdViaggio(),
                p.getIdTurista(),
                p.getStatoPrenatazione()
            });
        }
        
    }

    public void readJTableForDest(String stato) {
        DefaultTableModel modelo = (DefaultTableModel) tblPartecipanti.getModel();
        modelo.setNumRows(0);
        PartecipantiDAO pdao = new PartecipantiDAO();

        for (Partecipanti p : pdao.readForStato(stato)) {
            modelo.addRow(new Object[]{
                p.getIdPartecipanti(),
                p.getIdViaggio(),
                p.getIdTurista(),
                p.getStatoPrenatazione()
            });
        }
    }

