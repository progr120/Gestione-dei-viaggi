    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:

        Partecipanti p = new Partecipanti();
        PartecipantiDAO dao = new PartecipantiDAO();

        p.setIdViaggio(Integer.parseInt(txtIdViaggio.getText()));
        p.setIdTurista(Integer.parseInt(txtIdTurista.getText()));
        p.setStatoPrenatazione(txtStatoPrenotazione.getText());
        dao.create(p);

        txtIdViaggio.setText("");
        txtIdTurista.setText("");
        txtStatoPrenotazione.setText("");

        readJTable();

    }//GEN-LAST:event_btnCreateActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        if (tblPartecipanti.getSelectedRow() != -1) {

            Partecipanti p = new Partecipanti();
            PartecipantiDAO dao = new PartecipantiDAO();

            p.setIdViaggio(Integer.parseInt(txtIdViaggio.getText()));
            p.setIdTurista(Integer.parseInt(txtIdTurista.getText()));
            p.setStatoPrenatazione(txtStatoPrenotazione.getText());
            p.setIdPartecipanti((int) tblPartecipanti.getValueAt(tblPartecipanti.getSelectedRow(), 0));
            dao.update(p);

            txtIdPartecipanti.setText("");
            txtIdViaggio.setText("");
            txtIdTurista.setText("");
            txtStatoPrenotazione.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para atualizar");
        }


    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        if (tblPartecipanti.getSelectedRow() != -1) {

            Partecipanti p = new Partecipanti();
            PartecipantiDAO dao = new PartecipantiDAO();

            p.setIdPartecipanti((int) tblPartecipanti.getValueAt(tblPartecipanti.getSelectedRow(), 0));
            dao.delete(p);

            txtIdPartecipanti.setText("");
            txtIdViaggio.setText("");
            txtIdTurista.setText("");
            txtStatoPrenotazione.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para deletar");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

